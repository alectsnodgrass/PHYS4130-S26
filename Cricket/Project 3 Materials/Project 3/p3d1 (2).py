# RK2 Method
def RK2(N_initial, tmin, tmax, nts, deriv):

    # define initial variables
    N = np.zeros(nts + 1)
    N[0] = N_initial
    t = np.linspace(tmin, tmax, nts + 1)
    dt = t[1] - t[0]

    # loop the first and second substep
    for it in range(0, nts):
        N_half = N[it] + (dt / 2) * deriv(N[it], t[it])
        N[it+1] = N[it] + dt * deriv(N_half, t[it] + dt/2)

    return t, N

# Euler Method
def Euler_solver(N_initial, tmin, tmax, nts, deriv):

    # define initial variables
    N = np.zeros(nts + 1)
    N[0] = N_initial
    t = np.linspace(tmin, tmax, nts + 1)
    dt = t[1] - t[0]

    # loop
    for it in range(0,nts):
        N[it+1] = N[it] + dt * deriv(N[it], t[it])

    return t, N

# Scipy Method (updated for SHO)
def scipy_method(N_initial, tmin, tmax, nts, deriv):

    t = np.linspace(tmin, tmax, nts)
    N = odeint(deriv, N_initial, t)
    x = N[:,0]
    v = N[:,1]

    return t, x, v

# RK4(5) scipy function

def RK45_method(N_initial, tmin, tmax, nts, deriv):
  t = np.linspace(tmin, tmax, nts)

  sol = solve_ivp(deriv, (tmin, tmax), N_initial, method='RK45', t_eval=t)

  x = sol.y[0]
  v = sol.y[1]

  return t, x, v

