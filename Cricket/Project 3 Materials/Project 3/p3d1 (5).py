# import libraries
import matplotlib.pyplot as plt
import numpy as np
from scipy.integrate import odeint, solve_ivp

# Scipy Method (updated for SHO)
def scipy_method(N_initial, tmin, tmax, nts, deriv):

    t = np.linspace(tmin, tmax, nts)
    N = odeint(deriv, N_initial, t)
    x = N[:,0]
    v = N[:,1]

    return t, x, v

# RK4(5) scipy function

def RK45_method(N_initial, tmin, tmax, nts, deriv):
  t = np.linspace(tmin, tmax, nts)

  sol = solve_ivp(deriv, (tmin, tmax), N_initial, method='RK45', t_eval=t)

  x = sol.y[0]
  v = sol.y[1]

  return t, x, v

# Verlet-velocity sympletic integrator

def accel(x, t):
    return -(k/m)*x

def verlet(N_initial, tmin, tmax, nts, deriv):

  # define inital variables
  x0, v0 = N_initial
  x = np.zeros(nts + 1)
  v = np.zeros(nts + 1)
  x[0] = x0
  v[0] = v0
  t = np.linspace(tmin, tmax, nts + 1)
  dt = t[1] - t[0]

  # loop
  for i in range(nts):
    a = deriv(x[i], t[i])
    v_half = v[i] + 0.5 * dt * a
    x[i+1] = x[i] + dt * v_half
    a_new = deriv(x[i+1], t[i+1])
    v[i+1] = v_half + 0.5 * dt * a_new

  return t, x, v

# For a simple harmonic oscillator not at rest, what shape will it trace out in phase space?

# deriv functions
def deriv_ivp(t, N):
    x, v = N
    dxdt = v
    dvdt = -(k/m)*x
    return [dxdt, dvdt]

def deriv_odeint(N, t):
    x, v = N
    dxdt = v
    dvdt = -(k/m)*x
    return [dxdt, dvdt]

# define initial variables
