# How does a SHM evolve with a linear damping term?

# rewriting deriv and accel functions to fit damping term
# derivative functions will need a damping term in the derivative
def nderiv_ivp(t, N):
    x, v = N
    dxdt = v
    dvdt = -(k/m)*x - (b/m)*v
    return [dxdt, dvdt]

def nderiv_odeint(N, t):
    x, v = N
    dxdt = v
    dvdt = -(k/m)*x - (b/m)*v
    return [dxdt, dvdt]

# acceleration term will include velocity
def naccel(x, v):
    return -(k/m)*x - (b/m)*v

# updating verlet
def verlet(N_initial, tmin, tmax, nts, deriv):

  # define inital variables
  x0, v0 = N_initial
  x = np.zeros(nts + 1)
  v = np.zeros(nts + 1)
  x[0] = x0
  v[0] = v0
  t = np.linspace(tmin, tmax, nts + 1)
  dt = t[1] - t[0]

  # loop
  for i in range(nts):
    a = deriv(x[i], v[i])
    v_half = v[i] + 0.5 * dt * a
    x[i+1] = x[i] + dt * v_half
    a_new = deriv(x[i+1], v[i+1])
    v[i+1] = v_half + 0.5 * dt * a_new

  return t, x, v

# everything else will stay the same, but with a b term added
m = 1
